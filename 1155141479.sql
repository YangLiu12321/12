CREATE OR REPLACE VIEW TEMP AS
SELECT SP.SID, R.RID, SP.MARKET_VALUE, R.FOOTBALL_RANKING, 
(SUM(SPONSORSHIP)/(SQRT(SP.MARKET_VALUE) * LOG(2, (SQRT(R.FOOTBALL_RANKING) + 1) ))) AS HOT
FROM REGIONS R, LEAGUES L, SPONSORS SP, SUPPORT SU
WHERE R.RID=L.RID AND L.LID=SU.LID AND SU.SID=SP.SID
GROUP BY SP.SID, R.RID, SP.MARKET_VALUE, R.FOOTBALL_RANKING;
Spool result6.lst
SELECT SID, HOT 
FROM TEMP
WHERE HOT=(SELECT MAX(HOT) FROM TEMP WHERE FOOTBALL_RANKING < 10 AND MARKET_VALUE > 40)
      AND FOOTBALL_RANKING < 10 AND MARKET_VALUE > 40
ORDER BY SID DESC;
